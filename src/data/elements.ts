/**
 * Periodic table element data
 * Contains all 118 elements with their properties
 */

export type ElementCategory =
  | "alkali-metal"
  | "alkaline-earth"
  | "transition-metal"
  | "post-transition"
  | "metalloid"
  | "nonmetal"
  | "halogen"
  | "noble-gas"
  | "lanthanide"
  | "actinide";

export type ElementPhase = "solid" | "liquid" | "gas";

export interface Element {
  atomicNumber: number;
  symbol: string;
  name: string;
  nameIs: string; // Icelandic name
  atomicMass: number;
  category: ElementCategory;
  group: number | null; // 1-18, null for lanthanides/actinides
  period: number; // 1-7
  block: "s" | "p" | "d" | "f";
  electronegativity?: number;
  electronConfiguration: string;
  oxidationStates: number[];
  meltingPoint?: number; // Kelvin
  boilingPoint?: number; // Kelvin
  density?: number; // g/cm³
  discoveryYear?: number;
  description?: string;
}

/**
 * Get the phase of an element at room temperature (298K)
 */
export function getElementPhase(element: Element): ElementPhase {
  const roomTemp = 298; // Kelvin (~25°C)
  if (element.boilingPoint && element.boilingPoint < roomTemp) {
    return "gas";
  }
  if (element.meltingPoint && element.meltingPoint < roomTemp) {
    return "liquid";
  }
  return "solid";
}

